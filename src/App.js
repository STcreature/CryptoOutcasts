function App() {

  useEffect(() => {
    // Update the document title using the browser API
    initiateMetamask()
  }, []);


  const [btn_text, setBtnText] = useState("Connect Wallet")
  const [btn_color, setBtnColor] = useState("btn-green-big")
  const [btn_status, setBtnStatus] = useState(false)
  const [connectedAddress, setAddress] = useState()
  const [rangeValue, setRangeValue] = useState("4")
  const [mint_status, setMintStatus] = useState(true)
  const [mint_color, setMintColor] = useState("btn-disabled-big")
  const [totalminted, setTotalMinted] = useState(0)


  function initiateMetamask(){
    if (typeof window.ethereum !== 'undefined') {
    }
    else{
      setBtnStatus(true);
      setBtnText("Install Metamask");
      setBtnColor("btn-red-big")
    }
  }

  window.ethereum.on('accountsChanged', function (accounts) {
    setAddress(accounts[0])
  });


  const connectMetamask = async () => {
    if(typeof window.ethereum !== 'undefined'){
      try {
        const address = await window.ethereum.enable() //connect Metamask
        setAddress(address[0])
        setBtnText("Connected")
        setMintStatus(false)
        setMintColor("btn-green-big")
		const web3 = new Web3(window.ethereum);
		const contractAddr = '0xe6749d06beA9aBC495313CdB80586B102a3c1942';
		const OutcastsContract = new web3.eth.Contract(abi, contractAddr);
		setTotalMinted(await OutcastsContract.methods.totalSupply().call({from: connectedAddress}))

      }
      catch (error) {
        setBtnText("Refresh Page")
        setBtnColor("btn-warning-big")
        setBtnStatus(true)
      }
    }
    else {
      setBtnColor("btn-red-big")
      setBtnText("Install Metamask")
      setBtnStatus(true)
    } 
  }

  const initiateMint = async (numMint) =>{
    const web3 = new Web3(window.ethereum);
    const contractAddr = '0xe6749d06beA9aBC495313CdB80586B102a3c1942';
    const OutcastsContract = new web3.eth.Contract(abi, contractAddr);
    const price = "25000000000000000"
    const total = web3.utils.toBN(rangeValue * parseInt(price))
    await OutcastsContract.methods.preSaleMint(rangeValue).send({from: connectedAddress, value: total})
  }

  return (
    <div className="App">
      <div className="Header">
          <img src={imageLogo}></img>
          <Button btnText={btn_text} btnColor={btn_color} btnState={btn_status} onClickFunction={connectMetamask}></Button>
      </div>
      <div className="info">

        <h2>Choose your desired mint amount:</h2>
        <SliderCustom onChangeFunction={setRangeValue}></SliderCustom>
        <div className="value-container">
          <h3>Amount: {rangeValue}</h3>
        </div>
        <div className="mint">
          <Button btnText="MINT" btnColor={mint_color} btnState={mint_status} onClickFunction={initiateMint}></Button>
		  <div className="supply">
			  <h2 className="mints">{totalminted}/400 Minted.</h2>
		  </div>
        </div>
      </div>
    </div>
  );
}


export default App;
